      With a striking figure nearly six feet tall and the scrawny posture of a man that gave everything he had to others, he was the kind of person I initially aspired to be. </br>
      &nbsp;&nbsp;&nbsp;&nbsp;It had been two days before he woke up again. I had been catching as many insects as possible left in the decayed crops, eating some, and leaving the rest for the stranger. This was no sustainable solution, but given little vegetation we had left on our land, it was the only one. Using the last piece of his cloak, I bandaged his head for the seventh time. I watched him struggle but eventually manage to sit upright and greet me slightly more formally. As relieved as I was to discover he was okay, the burning guilt for what I had done irked me beyond any human limit. His name, I remember clearly to be John. Throughout his stay he told me of all countless wonders that lay beyond this desolate wasteland of suffering. Wonders of books that told of tales beyond imagination, castles that reached for the sky, higher than any man dreamed of, and people that sang and danced around fires listening to musical instruments.
