      I needed someone to talk to, someone trustworthy and intelligent - in this world, however, such a thing was beyond scarcity: it was non-existent. </br>
      &nbsp;&nbsp;&nbsp;&nbsp;For five years I had waited, until the day a blonde man walked up to my hay clothes stand. He fell to his knees, clothes in tatters, face streaked with blood, and look up at me as if feeling the utmost sorrow. With hardly a whisper I heard: "I'm sorry. I failed you. I carried books and food from countless lands for you but I lost it all. Any coin I gathered, I was beat for, and any love I shared I was taken advantage for. I've lost my faith and I don't deserve your forgiveness." Feeling my confusion and pain show, I remember only hugging this long lost friend until I realized that God had in fact abandoned us entirely. The human race was nothing but a greedy and hallow carcass of facades constructed from words, clothes, and masks. I pleaded until my throat was raw that he take me with him so that we never have to see this world again, and so he did. One step closer to where I am today, we set off to a location unknown to man. Far from any human voice, and just as far from any kings, gods, or supernatural miracles.


