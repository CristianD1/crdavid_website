      Having lived in the recluse of my shack and poverty, all this seemed wonderful to me. John taught me to make clothing and shoes using hay and spent a couple days further showing me how to trade them for food before waking me up one morning with the intent to leave. I remember how vividly I wished I was dead.</br>
      &nbsp;&nbsp;&nbsp;&nbsp;For five years he had left me with nothing but the thought that people were bound to ruin you, no matter how kind they were. These terrible words had thrown me into a swirling abyss of depression as my scrawny body slaved over endless bundles of hay. I created cloaks, hats, shoes, and even shirts out of this damned endless supply of inedible straw. Barely getting by each week on food and drink, my stomach had actually started to look fuller and I was resembling more of a human being than a starving worthless child. For five years I had stumbled alone in the dark in my miserably lonely shack, wondering what I had done to dissatisfy the Gods. I hated him. Both God and the blonde man. He left me here to rot when I begged that he stay. 

