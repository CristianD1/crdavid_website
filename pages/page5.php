<div class="row">
  <div class="large-12 columns">
    <div class="writing">
      Alone I tread in labyrinth deep,</br>
      Through deserts wide, and canyons steep,</br>
      In prison cells I rest alone -- and weep.</br>
      Alone and caged with no way out,</br>
      None see my plight, none hear my shout,</br>
      For stone hold not my front and back,</br>
      Nor distance block my forward track.</br>
      I'm lost at sea of heart and mind,</br>
      Known only to the deaf and blind.
