      &nbsp;&nbsp;&nbsp;&nbsp;Standing on the corner of an intersection, I waited for someone to come directly into view. He was wearing a long grey cloak that oddly enough seemed to complement his long wavy blonde hair. Before having enough time to notice if he was alone, I gripped my half broken bedpost and swung towards the back of his skull. With nary a moment too soon, it collided with a bloodcurdling thud and the only image that filled my eyes was that of the faces of the less fortunate as their eyes lit up at the sight of me and my parents bringing what little resources we had. What have I been reduced to? The man was sprawled on the floor desperately grasping his bleeding head as I searched under his cloak for something; anything. As my hands moved back, empty hot tears fell down my face hearing the man whisper shakily "I have nothing. I gave my last bit of food to the last few children down the road." He sat up, hands still clutching his head and quietly sobbed, apologizing repeatedly for not being able to give me anything. I sat back and realized that this man, for all I knew, must have been God himself.

