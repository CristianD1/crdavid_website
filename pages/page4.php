      Moonlight Sonnet
    </div>
  </div>

  <div class="row">
    <div class="large-12 columns">
      <div class="writing">
        The moon under cover of night invades,</br>
        A wash of pallid frosting coats the floor.</br>
        Through windows trespasses and permeates,</br>
        And folly, settles on my open door.
      </br></br>
        So do the mirrors ever take my gaze,</br>
        by the kind translucent haze of the night,</br>
        does the shine still never fail to amaze.</br>
        It sought to blanket naught but all in sight.
      </br></br>
        The sound of wolves in the distance do cry,</br>
        The banner beckons the darkness of night.</br>
        The laugh, resounding from the throne up high,</br>
        So taunts the sun in dark with feeble light.
      </br></br>
        And as he sinks to nightly darkness deep,</br>
        the moon does rise to sneer as fires weep.
      </div>
