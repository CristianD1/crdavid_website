      Having lived most of my life in this silence, I look back and yearn for the feeling I had in my first meaningful human interaction.
      My feet dragged on the cold concrete, leaving small white trails of dust. Eyes opening to the dim sunlight glazed floor, I screamed from the incredible amount of pain I felt washing over my entirety. I screamed and cried but my ears could hear no sound, nor could my throat distinguish between voice and croak. The other night I had been beaten to the point of near death for my miserably weak attempt at stealing food; but I somehow managed to walk back home. Before collapsing onto the floor, starving, my stomach growled at me for my defeat and spent the night making noise trying to feed itself. With the strength of a sloth I managed to stand and pick up what was left of a bedpost. Today was the day I fought for my food without holding back. With my parents having starved for my sake nearly a month ago, I was willing to sacrifice my well-being for whatever future event that might come my way.</br>

